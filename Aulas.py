#--------------------------------------Primeira Aula--------------------------------------#

# O que é logica?

# É o resultado de como usamos nosso raciocineo para descobrir as ações e a ordem dessas ações para cumprir uma atividade



# A atividade ou proposito definem a logica.

# EXEMPLO: Carroça na frente dos bois

# Do ponto de vista do objetivo (Levar algo na carroça de um lugar para o outro), a carroça faz
# sentido, o carrinho faz sentindo, o cavalo faz sentindo. O problema é a ordem. Se o carrinho 
# estiver na frente dos bois, o condutor não vai conseguir guiar, o cavalo não vai vai conseguir 
# virar sozinho. Ou seja a logica para essa atividade esta errada, porque a ordem está errada. 

# Do ponto de vista humoristico, essa metafora faz muito sentido e logica, pois ira gerar ocasionalmente uma tragedia, que vira humor.



# Com a ordem e a atividade claras, podemos explicar o que queremos fazer para o computador.



